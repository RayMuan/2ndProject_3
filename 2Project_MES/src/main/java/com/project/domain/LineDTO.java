package com.project.domain;

public class LineDTO extends OrderDTO{
	private String line_cd;
	private String line_nm;
	private String line_process;
	private String line_place;
	private int line_num;
	private String line_st;
	private String line_note;
	public String getLine_cd() {
		return line_cd;
	}
	public void setLine_cd(String line_cd) {
		this.line_cd = line_cd;
	}
	public String getLine_nm() {
		return line_nm;
	}
	public void setLine_nm(String line_nm) {
		this.line_nm = line_nm;
	}
	public String getLine_process() {
		return line_process;
	}
	public void setLine_process(String line_process) {
		this.line_process = line_process;
	}
	public String getLine_place() {
		return line_place;
	}
	public void setLine_place(String line_place) {
		this.line_place = line_place;
	}
	public int getLine_num() {
		return line_num;
	}
	public void setLine_num(int line_num) {
		this.line_num = line_num;
	}
	public String getLine_st() {
		return line_st;
	}
	public void setLine_st(String line_st) {
		this.line_st = line_st;
	}
	public String getLine_note() {
		return line_note;
	}
	public void setLine_note(String line_note) {
		this.line_note = line_note;
	}
	
	
}
